//实现删除字符串中连续的重复字符（除字母和数字）。
//
//输入为字符串，将字符串中连续重复的，不是字母且不是数字的字符删去，然后输出处理后的字符串。
//输入字符串最长50个字符，之后截断，只输出处理后的字符串。
//
//
//例如
//输入11+++2==13回车
//输出11+2=3
#include <stdio.h>
#include <string.h>
int main()
{
	char a[51]={0};
	int i,j;
	gets(a);
	for(i=0;*(a+i)!='\0';i++)
	{
		if((*(a+i)<'0' || (*(a+i)>'9')&&( *(a+i)<'A') || (*(a+i)>'Z')&&( *(a+i)<'a') || *(a+i)>'z'))
		{
			if(*(a+i+1)==*(a+i))
			{
				for(j=i;*(a+j)!='\0';j++)
				{
					*(a+j)=*(a+j+1);
				}
				i--;
			}
		}
	}
	puts(a);
}

