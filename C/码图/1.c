#include <stdio.h>
#include <string.h>
int main()
{
	//输入字符串
	char a[51]={0};//
	int i,j;
	gets(a);

	for(i=0;*(a+i)!='\0';i++)//判断每个字符
	{
		if((*(a+i)<'0' || (*(a+i)>'9')&&( *(a+i)<'A') || (*(a+i)>'Z')&&( *(a+i)<'a') || *(a+i)>'z'))//若此字符不是字母或数字
		{
			if(*(a+i+1)==*(a+i))//若下一字符与此字符相同
			{
				for(j=i;*(a+j)!='\0';j++)//将此字符之后所有字符左移一位
				{
					*(a+j)=*(a+j+1);
				}
				i--;//指针计数左移
			}
		}
	}
	puts(a);//输出字符串
}

